void astronomia() {
	
	int notes = sizeof(coffinDurations) / sizeof(coffinDurations[0]);

	int noteDuration = 0, period = 0;
	
	while(1) {
		
		for(int i = 0; i<notes; i+=1) {
			// calculates the duration of each note
			noteDuration = 1000 / coffinDurations[i];
			period = FREQ_2_MOD(coffinMelody[i]);
			TPM1->MOD = period;
			TPM1_C0V = period / 4; //12.5% duty cycle
			delay_mult40(10*noteDuration);
			TPM1->MOD = 0;
			TPM1_C0V = 0;
			delay_mult40(9*noteDuration);
		}
		
	}
}

